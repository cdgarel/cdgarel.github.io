// min /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function min(a, b) {
  
     if (a < b) {
      return a;
    } else if (b < a) {
      return b;
    } else {
    return 0;
}
}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
// single parameter, positive whole number, returns boolean
// tests if number is even

function isEven(num) {
  if (num >= 0) {
    if (num === 0 || num % 2 === 0) {
      return true;
    } else {
      return false; 
    } 
  } return false;
}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
// 2 parameters: a string, and a character
// returns number of times character is in string

function countChars(string, char) {
  let count = 0;
  for (let i = 0; i < string.length - 1; i++) {
    if (string[i] === char) {
      count = count + 1
    }
  } return count;
}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
// 1 parameter, a string
// counts how many uppercase Bs are in string

function countBs(string) {
    let count = 0;
    for (let i = 0; i < string.length; i++) {
      if (string[i] === "B") {
        count = count + 1;
      }
    } return count; 
}

